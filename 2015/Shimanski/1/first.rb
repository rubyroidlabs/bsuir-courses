n = CAR.max { |a, b| a.size <=> b.size }.size
s = CAR.size
system 'clear'

# выезжает
n.times do |i| # по длине строки
  s.times do |j| # кол-во строк
    if j < 8
      if i.even?
        puts SKY_1
      else
        puts SKY_2
      end
    else
      puts PODCH * (DLINA - i) + CAR[j][0..i]
    end
  end
  sleep(SLEEP_TIME)
  system 'clear'
end
# едет
while start != 0 # пока не врежется
  CAR.each_index do |i|
    if i < 8
      if start.even?
        puts SKY_1
      else
        puts SKY_2
      end
    else
      puts PODCH * start + CAR[i] + PODCH * (DLINA - start - CAR[i].size)
    end
  end
  start -= 1
  sleep(SLEEP_TIME)
  system 'clear'
end
# заезжает
n.times do |i|
  s.times do |j|
    if j < 8
      if i.even?
        puts SKY_1
      else
        puts SKY_2
      end
    else
      puts CAR[j][i..-1] + PODCH * (DLINA - n + i)
    end
  end
  sleep(SLEEP_TIME)
  system 'clear'
end
