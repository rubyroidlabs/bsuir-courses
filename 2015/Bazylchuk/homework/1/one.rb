Curses.init_screen
Curses.start_color
Curses.init_pair(COLOR_GREEN,COLOR_GREEN,COLOR_WHITE)
Curses.init_pair(COLOR_BLUE,COLOR_BLUE,COLOR_WHITE)
Curses.init_pair(COLOR_RED,COLOR_RED,COLOR_WHITE)
begin
  start=1
  fin=0
  spaces=Curses.cols-1               
  end_spaces=1
  cycles=Curses.cols+moto[0].length  
  cycles_number=0
  
  while cycles>0
    Curses.setpos(0, 0) 
    cycles_number+=1

    if cycles>Curses.cols   
      Curses.attron(color_pair(COLOR_GREEN)){  
        moto.each { |x| Curses.addstr(" " *spaces+ "#{x[0..fin]}") } #добавляем пробелы начала , т.е. смещаем рисунок вправо
      }  
      fin+=1
      spaces-=1
    end

    if cycles>moto[0].length && cycles<=Curses.cols
      Curses.attron(color_pair(COLOR_BLUE)){
        moto.each { |x| Curses.addstr(" " *spaces+ "#{x}" + " " *end_spaces) } # пробел начала уменьшаем и добавляем пробелы конца 
      }
      spaces-=1
      end_spaces+=1
    end

    if cycles<=moto[0].length 
      Curses.attron(color_pair(COLOR_RED)){
        moto.each { |x| Curses.addstr("#{x[start..moto[0].length]}" + " " *end_spaces) } # добавляем пробелы конца 
      }  
      start+=1
      end_spaces+=1
    end
    
    Curses.refresh
    cycles-=1
    sleep 0.02
  end

    Curses.close_screen
end